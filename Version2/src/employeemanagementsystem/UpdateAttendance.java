    private void UpdateEmployeeBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateEmployeeBActionPerformed
       
          try {
            // Get inputs from the user
            String name = NameTF.getText();
            int id = Integer.parseInt(IDTF.getText());
            String date = DateTF.getText();
            String timeIn = TimeInTF.getText();
            String timeOut = TimeOutTF.getText();
            
            // Call controller to update attendance
            boolean success = controller.updateAttendance(id, date, timeIn, timeOut);

            if (success) {
                JOptionPane.showMessageDialog(null, "Employee Attendance Updated");
                clearAll();
            } else {
                JOptionPane.showMessageDialog(null, "Failed to Update Employee Attendance. Please try again.");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "An error occurred. Please check your input.");
        }
        
       
        //clearAll();
    }//GEN-LAST:event_UpdateEmployeeBActionPerformed

