    private void SearchBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(IDTF.getText()); // Get the employee ID from input
            
            // Fetch attendance data using the controller
            Map<String, String> attendanceData = controller.fetchAttendanceById(id);
            
            if (!attendanceData.isEmpty()) {
                // Populate the fields with the retrieved data
                NameTF.setText(attendanceData.get("name"));
                DateTF.setText(attendanceData.get("attendance_date"));
                TimeInTF.setText(attendanceData.get("check_in_time"));
                TimeOutTF.setText(attendanceData.get("check_out_time"));
            } else {
                // Show an error message if no attendance data is found
                JOptionPane.showMessageDialog(null, "ID does not exist.");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "An error occurred. Please check your input.");
        }
        
                                           

    }//GEN-LAST:event_SearchBActionPerformed
