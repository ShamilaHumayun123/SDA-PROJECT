public class Summary {
    private int totalEmployees;
    private int totalDepartments;
    private double averageSalary;
    private int employeeId;
    private int totalLeavesTaken;
    private int totalBonusesReceived;
    private int departmentId;
    private int totalEmployeesInDepartment;

    // Getters and Setters
    public int getTotalEmployees() {
        return totalEmployees;
    }

    public void setTotalEmployees(int totalEmployees) {
        this.totalEmployees = totalEmployees;
    }

    public int getTotalDepartments() {
        return totalDepartments;
    }

    public void setTotalDepartments(int totalDepartments) {
        this.totalDepartments = totalDepartments;
    }

    public double getAverageSalary() {
        return averageSalary;
    }

    public void setAverageSalary(double averageSalary) {
        this.averageSalary = averageSalary;
    }

    public int getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(int employeeId) {
        this.employeeId = employeeId;
    }

    public int getTotalLeavesTaken() {
        return totalLeavesTaken;
    }

    public void setTotalLeavesTaken(int totalLeavesTaken) {
        this.totalLeavesTaken = totalLeavesTaken;
    }

    public int getTotalBonusesReceived() {
        return totalBonusesReceived;
    }

    public void setTotalBonusesReceived(int totalBonusesReceived) {
        this.totalBonusesReceived = totalBonusesReceived;
    }

    public int getDepartmentId() {
        return departmentId;
    }

    public void setDepartmentId(int departmentId) {
        this.departmentId = departmentId;
    }

    public int getTotalEmployeesInDepartment() {
        return totalEmployeesInDepartment;
    }

    public void setTotalEmployeesInDepartment(int totalEmployeesInDepartment) {
        this.totalEmployeesInDepartment = totalEmployeesInDepartment;
    }
}
