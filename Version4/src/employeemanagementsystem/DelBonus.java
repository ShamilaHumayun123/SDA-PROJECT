    private void DeleteEmployeeBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteEmployeeBActionPerformed
       
       try {
            int id = Integer.parseInt(IDTF.getText());   // Get the ID entered by the user

            // Call the controller to delete the bonus
            boolean success = controller.deleteBonus(id);

            if (success) {
                JOptionPane.showMessageDialog(null, "Bonus Deleted Successfully");
            } else {
                JOptionPane.showMessageDialog(null, "Failed to Delete Bonus. Please try again.");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "An error occurred while deleting the bonus.");
        }
        
        //clearAll();
    }//GEN-LAST:event_DeleteEmployeeBActionPerformed

