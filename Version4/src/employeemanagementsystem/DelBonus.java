    private void SearchBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBActionPerformed
        // TODO add your handling code here:
         try {
            int id = Integer.parseInt(IDTF.getText());  // Get the ID entered by the user

            // Call the controller to fetch bonus details by ID
            ResultSet rs = controller.fetchBonusById(id);
            int size = 0;
            while (rs.next()) {
                size++;
                // Populate fields with bonus data from the database
                NameTF.setText(rs.getString("name"));
                DetailTF.setText(rs.getString("description"));
                DateTF.setText(rs.getString("date"));
            }

            // Show a message if no bonus is found for the provided ID
            if (size == 0) {
                JOptionPane.showMessageDialog(null, "ID does not exist.");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "An error occurred while fetching the data.");
        }
                                

    }//GEN-LAST:event_SearchBActionPerformed

