    private void UpdateEmployeeBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateEmployeeBActionPerformed
       try{
           
         String name = NameTF.getText();
            int id = Integer.parseInt(IDTF.getText());
            String mobileNo = PhoneTF.getText();
            int salary = Integer.parseInt(SalaryTF.getText());
            String address = AddressTF.getText();
            String email = EmailTF.getText();

            // Call controller to update the employee
            boolean success = controller.updateEmployee(id, name, mobileNo, salary, address, email);

            // Provide feedback to the user
            if (success) {
                JOptionPane.showMessageDialog(null, "Employee Record Updated Successfully");
              //  clearAll();
            } else {
                JOptionPane.showMessageDialog(null, "Failed to Update Employee Record");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_UpdateEmployeeBActionPerformed

