    private void SearchBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBActionPerformed
        // TODO add your handling code here:
        try {
            int id = Integer.parseInt(IDTF.getText());

            // Fetch employee details using the controller
            Map<String, String> employeeDetails = controller.fetchEmployeeById(id);

            if (!employeeDetails.isEmpty()) {
                // Populate fields with fetched data
                NameTF.setText(employeeDetails.get("name"));
                PhoneTF.setText(employeeDetails.get("mobile_no"));
                SalaryTF.setText(employeeDetails.get("basic_salary"));
                AddressTF.setText(employeeDetails.get("address"));
                EmailTF.setText(employeeDetails.get("email"));
            } else {
                // Show error if no employee is found
                JOptionPane.showMessageDialog(null, "ID does not exist.");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        

    }//GEN-LAST:event_SearchBActionPerformed

