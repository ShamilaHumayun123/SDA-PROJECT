    private void UpdateEmployeeBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateEmployeeBActionPerformed
       
        try {
                    int id = Integer.parseInt(IDTF.getText());
                    String name = NameTF.getText();
                    String detail = DetailTF.getText();
                    String date = DateTF.getText();
                    String amount = AmountTF.getText();

                    if (controller.updateExpense(id, name, detail, date, amount)) {
                        JOptionPane.showMessageDialog(null, "Company Expense Updated Successfully");
                        clearAll();
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to Update Expense");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Invalid Input: " + ex.getMessage());
                }
          //clearAll();
    }//GEN-LAST:event_UpdateEmployeeBActionPerformed

