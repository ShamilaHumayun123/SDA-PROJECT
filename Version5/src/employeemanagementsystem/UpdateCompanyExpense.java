    private void SearchBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBActionPerformed
        // TODO add your handling code here:
        try {
                    int id = Integer.parseInt(IDTF.getText());
                    Map<String, String> expenseData = controller.getExpenseById(id);

                    if (!expenseData.isEmpty()) {
                        NameTF.setText(expenseData.get("name"));
                        DetailTF.setText(expenseData.get("description"));
                        DateTF.setText(expenseData.get("expense_date"));
                        AmountTF.setText(expenseData.get("expense_amount"));
                    } else {
                        JOptionPane.showMessageDialog(null, "ID does not exist.");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Invalid Input: " + ex.getMessage());
                }
        
                                           

    }//GEN-LAST:event_SearchBActionPerformed
